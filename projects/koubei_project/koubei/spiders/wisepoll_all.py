    def parse_details(self, response):
        if response.text.find('该车暂无数据!') < 0:
            c_num = response.xpath('//*[@class="cil-dls mystat11"]/dd[1]/text()').re("\d+")[0]
            d_num = response.xpath('//*[@class="cil-dls mystat12"]/dd[1]/text()').re("\d+")[0]
            b_num = response.xpath('//*[@class="cil-dls mystat13"]/dd[1]/text()').re("\d+")[0]
            adv = response.xpath('//*[@class="point-good"]/span/text()').extract_first().strip().split(',')
            adv = ",".join([x.strip() for x in adv])
            dis_adv = response.xpath('//*[@class="point-bad"]/span/text()').extract_first().strip().split(',')
            dis_adv = ",".join([x.strip() for x in dis_adv])
            like_p = response.xpath('//*[@class="col-star"]/@style').re("width:(.*?)\%;")[0]

            weidus = response.xpath('//*[@class="feature-select"]')
            for w in weidus:

                fid = w.xpath("@fid").extract_first()
                if fid:
                    kid = w.xpath("@kid").extract_first()
                    inid = w.xpath("@inid").extract_first()
                    weidu = w.xpath("text()").extract_first()


                    imps = response.xpath("//*[@fid='%s']" % fid)
                    for imp in imps:
                        sword = imp.xpath("@sword").extract_first()
                        sfword = imp.xpath("@sfword").extract_first()
                        feature_word_id = imp.xpath("@feature_word_id").extract_first()
                        taidu = imp.xpath("@class").extract_first()
                        if taidu.find("good") >= 0:
                            taidu = "good"
                        elif taidu.find("com") >= 0:
                            taidu = "comm"
                        elif taidu.find("bad") >= 0:
                            taidu = "bad"

                        meta ={
                            "familyname":response.meta['familyname'],
                            "familyid": response.meta['familyid'],
                            # "brand": response.meta['brand'],
                            "sword":sword,
                            "sfword":sfword,
                            "feature_word_id":feature_word_id,
                            "taidu":taidu,
                            "fid":fid,
                            "inid":inid,
                            "weidu":weidu,
                            "c_num":c_num,
                            "d_num":d_num,
                            "b_num": b_num,
                            "adv": adv,
                            "dis_adv": dis_adv,
                            "like_p": like_p,
                        }
                        url = "http://www.wisepoll.com/index.php/Home/Sentence/lists?kid=%s&mfid=%s&ffid=%s&status=-2&sword=%s&sort=1&feature_word_id=%s&inid=%s" % (kid, fid, fid, sword, feature_word_id, inid)
                        yield scrapy.Request(url=url, meta=meta, callback=self.parse_comments)


    def parse_comments(self, response):
        if response.text.find("没有更多数据!") < 0:

            next_url = response.xpath("//*[@class='page-list-tmp']/a[6]/@href").re("javascript:setPage\(\"(.*?)\"\)")[0]
            if next_url:
                yield scrapy.Request(url=response.urljoin(next_url), meta=response.meta, callback=self.parse_comments)

            comments = response.xpath("//*[@class='c-l-comments']")
            for comment in comments:
                item = WisepollItem()
                item['username'] = comment.xpath("div[1]/div[2]/text()").extract_first()
                item['content'] = "".join(comment.xpath("div[2]/p/span/a/text() | div[2]/p/span/a/span/text()").extract())
                item['h_content'] = comment.xpath("div[2]/p/span/a/span/text()").extract_first()
