                item['featuredata_id'] = comment.xpath("div[2]/em/span[2]/@featuredata_id").extract_first()
                item['thumb_sum'] = comment.xpath("div[2]/em/span[2]/em[3]/text()").extract_first()
                try:
                    item['postdate'] = comment.xpath("div[2]/em/span[1]/text()").re("\s(.*?)来源")[0].strip()
                    item['source'] = comment.xpath("div[2]/em/span[1]/text()").re("来源：(.*?)/")[0].strip()
                except Exception as e:
                    print(e)
                    item['postdate'] = "-"
                    item['source'] = "-"

                item['familyname'] = response.meta['familyname']
                item['familyid'] = response.meta['familyid']
                # item['brand'] = response.meta['brand']

                item['c_num'] = response.meta['c_num']
                item['d_num'] = response.meta['d_num']
